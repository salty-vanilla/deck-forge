type PlanPayload = Partial<Pick<BuildPresentationInput, "brief" | "deckPlan">>;
type BuildPayload = Partial<
  Pick<BuildPresentationInput, "brief" | "deckPlan" | "slideSpecs" | "assetSpecs">
>;
type BuildInputPayload = PlanPayload | BuildPayload;
type BuildResultPayload = Pick<BuildPresentationInput, "slideSpecs" | "assetSpecs">;
type AssetAcquisitionMode = "generate" | "retrieve" | "auto";

async function readJsonFromFile<T>(path: string): Promise<T> {
  const payloadRaw = await readFile(path, "utf8");
  return JSON.parse(payloadRaw) as T;
}

async function resolveBuildResult(
  payload: BuildInputPayload,
  brief: BuildPresentationInput["brief"] | undefined,
  deckPlan: BuildPresentationInput["deckPlan"] | undefined,
  acquisitionMode?: AssetAcquisitionMode,
): Promise<BuildResultPayload | null> {
  if (
    Array.isArray((payload as BuildPayload).slideSpecs) &&
    Array.isArray((payload as BuildPayload).assetSpecs)
  ) {
    return {
      slideSpecs: (payload as BuildPayload).slideSpecs ?? [],
      assetSpecs: (payload as BuildPayload).assetSpecs ?? [],
    };
  }

  if (!brief || !deckPlan) {
    return null;
  }

  const { slideSpecs } = await generateSlideSpecs({
    brief,
    deckPlan,
  });
  const { assetSpecs } = await generateAssetPlan({
    brief,
    slideSpecs,
    acquisitionMode,
  });

  return { slideSpecs, assetSpecs };
}

function createPresentationArtifact(input: {
  brief: BuildPresentationInput["brief"];
  deckPlan: BuildPresentationInput["deckPlan"];
  slideSpecs: BuildPresentationInput["slideSpecs"];
  assetSpecs: BuildPresentationInput["assetSpecs"];
}) {
  return buildPresentationIr({
    brief: input.brief,
    deckPlan: input.deckPlan,
    slideSpecs: input.slideSpecs,
    assetSpecs: input.assetSpecs,
  });
}

function getOptionValue(args: string[], names: string[]): string | undefined {
  for (let i = 0; i < args.length; i += 1) {
    const current = args[i];
    if (!current || !names.includes(current)) {
      continue;
    }

    const next = args[i + 1];
    if (!next || next.startsWith("-")) {
      return undefined;
    }

    return next;
  }

  return undefined;
}

function getFlag(args: string[], name: string): boolean {
  return args.includes(name);
}

